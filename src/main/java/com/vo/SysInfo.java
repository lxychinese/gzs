package com.vo;

public class SysInfo {

	private int sysid;
	private String sysName;

	public SysInfo() {
	}

	public int getSysid() {
		return sysid;
	}

	public void setSysid(int sysid) {
		this.sysid = sysid;
	}

	public String getSysName() {
		return sysName;
	}

	public void setSysName(String sysName) {
		this.sysName = sysName;
	}
}
